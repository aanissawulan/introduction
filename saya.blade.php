  <p style="font-family:papyrus;" title="2. Gilderoy Lockhart">

  It is still something of a miracle that the flamboyant, humble-bragging Gilderoy Lockhart ever got the gig as Hogwarts’ new Defence Against the Dark Arts teacher in the first place, but you can’t deny the man had charisma. After nearly fooling everyone thanks to his lovely, blonde, tumbling tresses and his endlessly unrealistic sounding novels (Year with the Yeti, Gadding with Ghouls…) Lockhart’s tenure came to a dramatic close when he erased his own memory with Ron’s broken wand. Kind of sad, but on the other hand…

