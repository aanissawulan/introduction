<h2 style="color:black;text-align:center;font-family:justaword;"> 1. Dobby, naturally</h2>
<p style="font-family:papyrus;text-align: center;">
Probably one of our utmost favourite things about the entire Harry Potter series put together is Dobby; the bravest house-elf that ever bashed himself on the head. This is the book where we first meet Harry’s new, well-meaning, but also quite calamitous friend, when he turns up at Privet Drive begging him not to return to Hogwarts. Despite constant rebuffs, Dobby turns up at numerous unwanted times to remind Harry that he’s in great danger, etc – not that Harry ever listened. As we quickly learn, Dobby had Harry’s best interests at heart, and a friendship was born: not to mention, a great love of socks.

	
 </p>
